public void calcularGen(){ 
   String text = txtInversion.getText();
   
       if(text.matches("\\d+(\\.\\d{1,2})?")){
            inversion =Float.parseFloat(txtInversion.getText().trim());
            if(0<inversion){
            //Calculos por campos
            float alquiler =inversion*23/100;
            float publicidad =inversion*7/100;
            float transporte =inversion*26/100;
            float servicio =inversion*12/100;
            float decoracion =inversion*21/100;
            float gastos =inversion*11/100;
            //retornamos los valores
                DecimalFormat vf = new DecimalFormat("0.00");
           lblAlquiler.setText("S./ "+vf.format(alquiler));
           lblPublicidad.setText("S./ " +vf.format(publicidad));
           lblTransporte.setText("S./ " +vf.format(transporte));
           lblServicios.setText("S./ "+vf.format(servicio) );
           lblDecoracion.setText("S./ " +vf.format(decoracion));
           lblGastosVarios.setText("S./ " +vf.format(gastos));
           lblInversion.setText("Inv: S/"+vf.format(inversion));
            }else{
            JOptionPane.showMessageDialog(this, "No se aceptan inversiones menor a 0 ");
            
            }
       }else{
       JOptionPane.showMessageDialog(this, "Solo se pueden ingresar numeros positivos con 2 decimales");
       }
  txtInversion.setText("");
  }


