    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
         float inversion ;
        inversion=Float.parseFloat(txtInversion.getText().trim());
        
        float alqui = inversion*23/100;
        float publi = inversion*7/100;
        float transpor = inversion*26/100;
        float servis = inversion*12/100;
        float deco = inversion*21/100;
        float costos = inversion*11/100;
        
        lblAlquiler.setText("S/ "+ alqui);
        lblPublicidad.setText("S/ "+ publi);
        lblTransporte.setText("S/ "+ transpor);
        lblDecoracion.setText("S/ "+ deco);
        lblGastos.setText("S/ "+ costos);
        lblServicios.setText("S/ "+ servis);
    }//GEN-LAST:event_btnCalcularActionPerformed

