    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        short cantidad;
        float Precio=15;
        
        cantidad=Short.parseShort(txtCal.getText().trim());
        
        float subtotal = cantidad*Precio;
        float descuentoUNO =subtotal*7/100;
        float descuentoDOS =subtotal*93/100*7/100;
        float total =subtotal*93/100*7/100;
        //retornamos
        lblSubtotal.setText("S/ "+ subtotal );
        lblDescuento1.setText("S/ "+ descuentoUNO );
        lblDescuento2.setText("S/ "+ descuentoDOS );
        lblTotal.setText("S/ "+ total );
        txtCal.setText("");
    }//GEN-LAST:event_btnCalcActionPerformed

 
